function changeTabel(panelFirst, index) {
  var tbody = '';
  var TableType;
  if (panelFirst === 0) {
    TableType = 'await'
  } else if (panelFirst === 1) {
    TableType = 'push'
  } else if (panelFirst === 2) {
    TableType = 'deal'
  }

  var length = data[TableType][index].length;
  for (var i = 0; i < length; i++) {
    tbody += '<tr>' +
      '<td><input type="checkbox"></td>' +
      '<td>' + data[TableType][index][i].id + '</td>' +
      '<td>' + data[TableType][index][i].name + '</td>' +
      '<td>' + data[TableType][index][i].cardId + '</td>' +
      '<td>' + data[TableType][index][i].props + '</td>' +
      '<td>' + data[TableType][index][i].content + '</td>' +
      '<tr>'
  };
  if (panelFirst === 0) {
    $('.popup-tbody-1').html(tbody);
  } else if (panelFirst === 1) {
    $('.popup-tbody-2').html(tbody);
  } else if (panelFirst === 2) {
    $('.popup-tbody-3').html(tbody);
  }
}
$(function () {
  var panelFirst = 0;
  changeTabel(panelFirst, 0)
  $('.container-popup-first .push-panel').on('click', function () {
    $('.container-popup-first').hide();
    $('.container-popup-two').show();
  });
  $('.container-popup-two .confirm .cancel').on('click', function () {
    $('.container-popup-first').show();
    $('.container-popup-two').hide();
  });
  $('.container-popup-two .confirm .first').on('click', function () {
    $('.container-popup-two').hide();
    $('.container-popup-first').hide();
  });
  // 切换一级菜单
  $('.container-popup-first .nav li').on('click', function () {
    panelFirst = $(this).index();
    changeTabel(panelFirst, 0)
    $(this).siblings().removeClass("active");
    $(this).addClass('active');
    $(this).parents(".container-popup-first").find(".content-popup").removeClass("show");
    $(this).parents(".container-popup-first").find(".content-popup").eq(panelFirst).addClass("show");
  })
  // 切换二级菜单
  $('.button-popup .btn').on('click', function () {
    var index = $(this).index();
    var tbody = '';
    var length;
    changeTabel(panelFirst, index);
    $(this).siblings().removeClass("first");
    $(this).addClass('first');
  })
})

